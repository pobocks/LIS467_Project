<section id="timeline">
<h2>&nbsp;<a name="1800"><b><u>1800s: The beginning of T. B. Scott Library</u></b></a></h2>

<div><b>1886</b></div>
<div> T. B. Scott, lumberman, state Senator, and first mayor of Merrill dies.     Scott wills $10,000 to the city to found a free public library,  contingent upon the City’s provision of “suitable quarters” for the library  within five years.</div>   

<div><b>1889</b></div>
<div>Special city election is held to establish a public library. Results:  82 for; 0 opposed.</div>

<div><b>1891 </b></div>
<div>Janet Russell begins duties as first librarian on March 24. T. B. Scott Free Library opens on first floor of City Hall (now the Old City Hall Apartments), among the first two dozen public libraries in the state.  Previous to this time the community was served by rental libraries.  </div>

<div><b>1898</b></div>
<div>Merrill Traveling Library Association, one of the first in the state, is formed.  Communities throughout the county subscribe, including Tripoli, Russell, Gleason, Irma, Chat, and Heinemann.</div>    

<div><b>1899</b></div>   
<div>Classification of books changed from a “good ordinary classification”  to the Dewey Decimal system.</div>      

<h2>&nbsp;<a name="1900"><b><u>1900-1959: Becoming a Carnegie Library</u></b></a></h2>

<div><b>1900</b></div>
<div>Librarian authorized to maintain open shelves “to give the public opportunities to select their books by examination of them on the shelves.”</div>

<div><b>1901</b></div>
<div>Separate “Children’s Room” approved by Library Board.</div>

<div><b>1905</b></div>
<div>Helen Price begins duties as second librarian.  By spring she starts the  first English language classes for foreigners conducted in a Wisconsin public library.</div>

<div><b>1908</b></div>
<div>Katherine Barker begins duties as third librarian, serves until 1914.</div>

<div><b>1909</b></div>
<div>Request for $17,500 from Andrew Carnegie Foundation approved.    Claude & Starck of Madison (associates of the famed Louis Sullivan)   contracted as architects the following spring.</div>

<div><b>1910</b></div>
<div>Task of re-cataloging the library collection begins.</div>

<div><b>1911</b></div>               
<div>New Carnegie library completed in Stange’s Park. (Moved in August 21, 1911.)</div>

<div><b>W.W.I.</b></div>
<div>Winnifred Bailey, fourth librarian, takes active role in the war effort,  including national mobilization of library materials for American soldiers.</div>

<div><b>1917</b></div>
<div>Elisabeth Burke, fifth librarian, begins duties.</div>

<div><b>1919</b> </div>
<div>Edna D. Orr, sixth librarian, begins duties.</div>

<div><b>1922</b></div>
<div>Nathalie Scribner, seventh librarian, eventually becomes the chief architect of the Wisconsin Valley Library Association, formed to encourage the sharing of resources among member libraries.</div>

<div><b>1926</b></div>
<div>Thomas B. Scott, Jr., donates $10,000 to the library.  Interest from the gift to be used for books and magazines.</div>

<div><b>1929</b> </div>
<div>Due to growth in services and collections, Children’s Dept. moved to the remodeled lower level of the Carnegie building.  </div>

<div><b>1935</b></div>
<div>WPA grant helps with roofing and cement work on the library.</div>

<div><b>1945</b></div>
<div>H. V. Kaltenborn, Merrill native and famous radio commentator, journalist and author donates $2,000 to the library for creation of a book fund.</div>     

<div><b>1948</b></div>
<div>Edna Kraft begins duties as eighth librarian.</div>

<div><b>1949</b> </div>
<div>Several centers set up in the county so that rural residents don’t have to travel directly to the main library for books.</div>

<div><b>1959</b></div>
<div>The library wins a John Cotton Dana Honorable Mention Award for its  comprehensive, outstanding and original methods of service. [An American Library Association Award sponsored by the H. W. Wilson Co.]  </div>

<h2>&nbsp;<a name="1960"><b><u>1960 - 1999: Leaderships and Awards</u></b></a></h2>

<div><b>1961</b></div>
<div>The library begins participation in Project 6, a federally funded demonstration project of the Wisconsin Free Library Commission and the public libraries of Florence, Forest, Langlade, Lincoln, Oneida, and Vilas counties.  The project strengthens the collections of established libraries and uses a bookmobile to provide service to areas where there is no public library.</div>

<div><b>1966</b> </div>
<div>Architects hired to plan an addition to the library.</div>

<div><b>1965</b></div>
<div>T. B. Scott Library becomes a founding member of the new 	cooperative organization called  the Wisconsin Valley Library Reference Service.  Following passage of state legislation aimed at the formation of library systems, in 1972 this  organization became the Wisconsin Valley Library Service.</div>

<div><b>1968</b> </div>
<div>Edna Kraft named Librarian of the Year by the Wisconsin Library Association.</div>

<div><b>1969</b></div>
<div>Library addition opened, doubling the size of the building.  Architects were Foster & Shavey; Wausau, WI.</div>

<div><b>1970</b></div>
<div>Ramon Hernandez begins duties as ninth library director.</div>

<div><b>1971</b></div>
<div>Library acclaimed as an excellent example of Sullivanesque architecture by Frank Lloyd Wright Foundation and Wisconsin Chapter of American Institute of Architects.</div>

<div><b>1972</b></div>
<div>Library recognized by State Department of Natural Resources as an excellent example of construction on a flood plain.
Merrill receives its first State Aids funds ($7,881.50) for the expansion of library services.</div>

<div><b>1973 </b> </div>
<div>Gleason Branch opened.  Bonnie Preuser staffs the branch until it closes in 2001.</div>
<div>The library approved as a Wisconsin Registered Landmark.  </div>

<div><b>1974</b> </div>
<div>Library named to National Register of Historic Places.
Outreach Extension Service for the homebound, elderly and handicapped begins, funded with a federal grant.</div>

<div><b>1975</b> </div>
<div>Kathleen Gosz begins duties as tenth library director.</div>

<div><b>1976</b></div>
<div>Tile roof on Carnegie building restored; Bicentennial “roof raising” celebration.</div>

<div><b>1977 </b> </div>
<div>Library selected to receive the Wisconsin Library Association’s Clarence B. Lester Memorial Award (Wisconsin’s Library of the Year) as an “outstanding example of what a library can do and be in a small community.”</div>

<div><b>1981</b></div>               
<div>N. Curtis LeMay begins duties as eleventh library director.</div>

<div><b>1983</b></div>              
<div>Paperback book deposit station begun in Irma at the Irma Stop ‘N’ Shop.  The station operated until 1991. </div> 

<div><b>1985</b> </div>             
<div>First card catalog cards, book cards, and book pocket labels made with a new Apple IIe microcomputer.</div>

<div><b>1986</b> </div>             
<div>Anita Gebert, member of the Board of Trustees since 1960, named  State Library Trustee of the Year.</div>

<div><b>1988</b></div>              
<div>Library begins putting records of holding into statewide computer Database, WISCAT.</div>
<div>Beatrice Lebal begins duties as twelfth library director.</div>

<div><b>1991</b> </div>           
<div>Library celebrates its centennial, with a community-wide “Birthday Party in the Park.” </div>

<div><b>1993 </b> </div>            
<div>Card catalog discarded; use of Dynix computerized circulation system begins.</div>

<div><b>1994 </b> </div>            
<div>Patrons begin using the Dynix computerized catalog. </div> 

<div><b>1997</b> </div>        
<div>Library begins offering public Internet access.</div>

<div><b>1998</b></div>   
<div>Medford Public Library’s collection and patron records added to T. B. Scott Library’s computer database (the “M and M Project”).</div>

<div><b>1999</b></div>              
<div>Referendum for a library building project defeated.</div>
<div>T. B. Scott Library Board of Trustees gives its approval for the library to  become a founding member of V-Cat, a shared automation consortium administered by WVLS.</div>
<div>Tile roof on Carnegie building replaced.</div>

<div><b>1999</b></div> 
<div>Fundraising for a library building project begins.</div>
<div>Friends of T. B. Scott Library organized.</div>

<h2>&nbsp;<a name="2000"><b><u>2000 - today: Restoration and modernization</u></b></a></h2>

<div><b>2000</b> </div>
<div>Second referendum on the proposed library building project passes.
Library moved into temporary quarters at the former Fox Point factory, 1905 E. 14th St., during construction of the addition.</div>

<div><b>2001</b></div>
<div>Library moved back into the newly expanded and renovated building, and reopened the doors on Aug. 20th.</div>
<div>Library Web Page debuts.</div>

<div><b>2002</b></div>
<div>Library designated a Merrill Historic Landmark.</div>
<div>For the second time the library receives the Wisconsin Library Association’s Library of the Year award.  </div>

<div><b>2006</b></div>
<div>Beyond Books: Community Artshare begins.</div>

<div><b>2007</b></div>
<div>March: Library begins to offer wireless internet access to the  public. </div>       
<div>April: Lighted promotional sign, donated by Church Mutual launched.</div>
<div>October: Director Bea Lebal receives the Wisconsin Library Association&apos;s Muriel Fuller award.</div>

<div><b>2009</b></div>
<div>January: Stacy Stevens begins duties as the 13th Library Director.</div>
<div>September: Sunday hours begin after Labor Day.</div>

<div><b>2011</b></div>
<div>All year celebration of the 100th anniversary of the Carnegie Building.</div>
