<h2> Accessibility at the library </h2>
<p> T.B. Scott Library is A.D.A. compliant, with handicap parking and accessible entrance, wheelchair accessible elevator, and wheelchair accessible restrooms on three levels. Patrons can borrow wheelchairs, walkers with baskets and drop down seats, and mini shopping carts for in-building use at the front desk.</p>

<h2> Materials and Equipment </h2>
<p> Materials and equipment useful to disabled and elderly can be found on the third floor. Our collection includes: Large Print books, Audio books on cassette, Audio books on CD, Zoom text computer, Robot Reader, visual enlarger and color contrast video machine. </p>

<h2>Outreach to Seniors/Homebound</h2>
<p>If you are home bound, temporarily or permanently, the library will arrange to have materials delivered to you. Call (715) 536-7191 and leave a message for the outreach coordinator, Sandra Lussenhop, or e-mail her at <a href="sluss@wvls.lib.wi.us">sluss@wvls.lib.wi.us</a>.</p>

<h2>Computer Coaching</h2>  
<p>Get help with Internet, email, hardware and software at your pace. 
Computer Coaching happens Tuesday, Wednesday, and Thursday, of the second full week of every month.</p>
<ul id="computercoaching">
<li>Tuesdays, 10:00 - 11:30 am</li>
<li>Wednesdays, 6:00 - 7:30 pm</li>
<li>Thursday, 1:00 - 2:30 pm</li>
</ul>
<p>Since seating is limited, registration is recommended but not necessary; call (715) 536-7191 to reserve a seat or stop at the Adult Reference Desk on the 3rd floor. There is no cost to participate. Beginners especially welcome.</p>



